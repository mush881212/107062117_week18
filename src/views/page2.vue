          description: "辛明峰室內樂大師班",
          link: "https://art.nthu.edu.tw/?p=1971",
        },
      ],
      isPlaying: false,
      event_below: false,
    };
  },
  created() {
    document.addEventListener("scroll", this.eventScroll);
    window.scrollTo(0, 0);
  },

  methods: {
    eventScroll() {
      this.fadeIn();
      if (window.scrollY > 10 && !this.isPlaying) {
        this.isPlaying = true;
        var textWrapper = document.querySelector(".ml6 .letters");
        textWrapper.innerHTML = textWrapper.textContent.replace(
          /\S/g,
          "<span class='letter'>$&</span>"
        );
        this.$anime
          .timeline({ loop: false })
          .add({
            targets: ".ml6 .letter",
            translateY: ["1.1em", 0],
            translateZ: 0,
            duration: 1500,
            delay: (el, i) => 50 * i,
          })
          .add({
            targets: ".ml6",
            opacity: 1,
            duration: 1000,
            easing: "easeOutExpo",
            delay: 1000,
          });
      } else if (window.scrollY < 10) {
        this.isPlaying = false;
      }
    },
    fadeIn() {
      var events = document.getElementsByClassName("events-1");
      if (window.scrollY >= 10 && !this.event_below) {
        events.forEach((element) => {
          element.classList.remove("invisible");
          element.classList.add("fade-in");
        });
        this.event_below = true;
      } else if (window.scrollY < 10) {
        events.forEach((element) => {
          element.classList.add("invisible");
          element.classList.remove("fade-in");
        });
        this.event_below = false;
      }
    },
  },
};
